static void print_logo_narrow(void) {
    oled_clear();
    render_logo();

    /* wpm counter */
    uint8_t n = get_current_wpm();
    char    wpm_str[4];
    oled_set_cursor(0, 6);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);

    oled_set_cursor(0, 7);
    oled_write(" wpm", false);
        // WPM 바
    oled_set_cursor(0, 8);
    for (uint8_t i = 0; i < (current_wpm / 5) && i < 20; i++) {
        oled_write_P(PSTR("="), false);
    }
}

